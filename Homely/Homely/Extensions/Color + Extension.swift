import SwiftUI

extension Color {
    
    static func homeColor() -> Color {
        return Color("HomeColor")
    }
    
    static func familyColor() -> Color {
        return Color("FamilyColor")
    }
    
    static func personColor() -> Color {
        return Color("PersonColor")
    }
}
